int	ft_square(int n)
{
	return (n * n);
}

int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	while (ft_square(i) <= nb)
	{
		if (ft_square(i) == nb)
			return (i);
		i++;
	}
	return (0);
}
